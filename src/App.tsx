function processData(data, tutorId) {
  const [headerRow] = data;
  const courseData = [];

  for (const row of data.slice(1)) {
    if (row[1] === 'Done & Published') {
      courseData.push({
        id: row[0],
        name: row[2],
        grade: row[3]
      });
    }
  }

  // Save courses to Firestore
  await saveCourses(tutorId, courseData);
}

async function saveCourses(tutorId, courseData) {
  const batch = writeBatch(db);

  for (const course of courseData) {
    const ref = doc(db, 'tutors', tutorId, 'courses', course.id);
    batch.set(ref, { name: course.name, grade: course.grade });
  }

  await batch.commit();
}

